class MinStack:

    def __init__(self):
        self.stack = deque()
        

    def push(self, val: int) -> None:
        self.stack.append(val)
        

    def pop(self) -> None:
        self.stack.pop()
        

    def top(self) -> int:
        return self.stack[-1]
        

    def getMin(self) -> int:
        Min = self.stack[0]
        for val in self.stack:
            if val < Min:
                Min = val
        return Min
        


